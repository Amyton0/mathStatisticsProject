function onGroupChange(event) {
    let group = event.target.value;
    let block = document.getElementsByClassName('table_block')[0];
    let table = document.createElement('table');

    let header = table.insertRow();
    header.insertCell();

    for (let i = 0; i < students.dates.length; i++) {
        let cell = header.insertCell();
        cell.textContent = students.dates[i];
    }

    for (let name in students[group]) {
        let row = table.insertRow();
        let cell = row.insertCell();
        cell.textContent = name;
        for (var j = 0; j < students.dates.length; j++) {
            cell = row.insertCell();
            var input = document.createElement('input');
            input.type = 'text';
            input.placeholder = 'Введите значение';
            console.log(students[group][name][j])
            input.value = students[group][name][j]
            input.setAttribute('oninput', `handleInput(this, '${name}', '${group}', ${j})`);
            input.removeAttribute('placeholder');
            cell.appendChild(input);
        }
    }

    block.appendChild(table);
}

